class LiteralsExample {
    public static void main(String[] args) {
        // Integer literal
        int intLiteral = 100;
        
        // Floating-point literal
        double doubleLiteral = 99.99;
        
        // Character literal
        char charLiteral = 'A';
        
        // String literal
        String stringLiteral = "Hello, Java!";
        
        // Boolean literal
        boolean booleanLiteral = true;
        
        // Hexadecimal and Binary literals
        int hexLiteral = 0x1A;  // 26 in decimal
        int binaryLiteral = 0b1101; // 13 in decimal
        
        // Displaying the literals
        System.out.println("Integer Literal: " + intLiteral);
        System.out.println("Double Literal: " + doubleLiteral);
        System.out.println("Character Literal: " + charLiteral);
        System.out.println("String Literal: " + stringLiteral);
        System.out.println("Boolean Literal: " + booleanLiteral);
        System.out.println("Hexadecimal Literal: " + hexLiteral);
        System.out.println("Binary Literal: " + binaryLiteral);
    }
}
